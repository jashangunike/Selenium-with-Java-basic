public class StringConcatenation {
    public static void main(String[] args) {

        int i = 200 ;
        int j = 300 ;

        String s1 = " Jashan ";
        String s2 = " From patiala";

        System.out.println(i+j);
        System.out.println(s1+s2);
        System.out.println(i+j+s1+s2);
        System.out.println(i+j+(s1+s2));

    }
}
